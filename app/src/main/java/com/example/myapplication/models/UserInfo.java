package com.example.myapplication.models;

public class UserInfo {
    String name;
    String position;
    String branch;
    String sex;
    String portraitId;

    public String getName(){
        return name;
    }

    public String getPosition() {
        return position;
    }

    public String getBranch() {
        return branch;
    }

    public String getGender() {
        return sex;
    }

    public String getPortraitId() {
        return portraitId;
    }
}